package com.green.day6.ch3;

public class OperatorEx19 {
    public static void main(String[] args) {
        int x = 10;
        int y = 9;
        System.out.printf("x / y = %d, x %% y = %d\n", x / y, x % y);

        int x2 = 7;
        System.out.printf("x2 / y = %d\n", x2 / y);
        System.out.printf("x2 %% y = %d\n", x2 % y);
    }
}
