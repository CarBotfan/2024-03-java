package com.green.day8;

public class FlowEx12 {
    public static void main(String[] args) {
        /*
        반복문을 사용하여 1 ~ 5를 세로로 출력
        가로로 출력
         */
        for(int i = 1; i < 6; i++) {
            System.out.printf("%d\n", i);
        }
        for(int i = 1; i < 6; i++) {
            System.out.printf("%d ",i);
        }
        System.out.println();
    }
}
