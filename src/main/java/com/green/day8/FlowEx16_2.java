package com.green.day8;

public class FlowEx16_2 {
    public static void main(String[] args) {
        int star = 5;
        for(int i = 0; i < star; i++) {
            for(int j = 0; j < star; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
